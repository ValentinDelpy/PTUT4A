package model;

/**
 *
 * @author Soul
 */
public class Drug extends Treatment{
    
    private String name;

    public Drug(String name) {
        super(name);
    }
    
    @Override
    public String getName() {
        return name;
    }

    @Override
    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Intervention" + this.name;
    } 
}
